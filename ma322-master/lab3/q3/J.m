function [J] = J(x)
	J = [0, 0; 0, 0];
	J(1,1) = 6;
	J(1,2) = 2 * x(3) * sin(x(2) * x(3));
	J(1,3) = 2 * x(2) * sin(x(2) * x(3));
	J(2,1) = x(1) * (x(1)^2 + sin(x(3)) + 1.06)^(-1/2);
	J(2,2) = 9;
	J(2,3) = cos(x(3)) * (x(1)^2 + sin(x(3)) + 1.06)^(-1/2) / 2;
	J(3,1) = -3 * x(2) * exp(-x(1) * x(2));
	J(3,2) = -3 * x(1) * exp(-x(1) * x(2));
	J(3,3) = 60;
end